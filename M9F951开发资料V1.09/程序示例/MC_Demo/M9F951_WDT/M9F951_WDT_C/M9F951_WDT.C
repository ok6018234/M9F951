void SYSTEM_INIT(void);					//系统初始化

//=============================================================================
//********************************程序主函数***********************************
//=============================================================================
void main(void)					  		//程序主函数
{
	SYSTEM_INIT();						//系统初始化
	OSCM = 0x05;						//休眠模式下关闭高、低频时钟
	while(1)
	{
		STOP = 1;
		if(IOB0==1)
		{
			IOB0=0;
		}
		else 
		{
			IOB0=1;
		}
	}
}
